value = int(input("Digite um valor inteiro: "))

while value - 100 >= 0:
    print("Nota de 100!")
    value -= 100
while value - 50 >= 0:
    print("Nota de 50!")
    value -= 50
while value - 20 >= 0:
    print("Nota de 20!")
    value -= 20
while value - 10 >= 0:
    print("Nota de 10!")
    value -= 10
while value - 1 >= 0:
    print("Nota de 1!")
    value -= 1